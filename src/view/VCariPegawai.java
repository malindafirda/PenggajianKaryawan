   void isitabel(){
       Object data[][] = new Object[record.size()][4];
       int x = 0;
       for(ModelPegawai pegawai : record){
           
           data[x][0] = pegawai.getIdpegawai();
           data[x][1] = pegawai.getNama();
           data[x][2] = pegawai.getJabatan();
           data[x][3] = pegawai.getShift();
           
           x++;
       }
       
       String judul[]= {"idpegawai","nama","jabatan","jamkerja"};
      jtblpegawai.setModel(new DefaultTableModel(data,judul));
   }
   
   void refreshdata(){
       panggildata();
       isitabel();
   }

