    private void edcariKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_edcariKeyPressed
        // TODO add your handling code here:
 DefaultTableModel tampil_tbl = new DefaultTableModel();
 tampil_tbl.addColumn("ID PEGAWAI");
 tampil_tbl.addColumn("Nama Pegawai");
 tampil_tbl.addColumn("Jabatan");
 tampil_tbl.addColumn("Jam Kerja");
 
        try { 
            java.sql.Connection ambil = new KoneksiDB().getConnect();
            java.sql.Statement stmt = ambil.createStatement();
            
        String sql = "SELECT * FROM tbl_pegawai where idpegawai like'%"+edcari.getText() +"%'";
        java.sql.ResultSet rs = stmt.executeQuery(sql);
        
        while (rs.next()) {
            
            tampil_tbl.addRow(new Object[]{
                rs.getString(1),
                rs.getString(2),
                rs.getString(3),
                rs.getString(4)
                });
          }
        
           jtblpegawai.setModel(tampil_tbl); 
       
        } catch (Exception e) {
        }
    }//GEN-LAST:event_edcariKeyPressed

